/**
 * Get all ships of a specific role
 */
export function getShipsByRole(role) {
    return Object.entries(SHIP_DATABASE)
        .filter(([, ship]) => ship.role === role)
        .reduce((acc, [id, ship]) => { acc[id] = ship; return acc; }, {});
}

/**
 * Get all ships of a specific size
 */
export function getShipsBySize(size) {
    return Object.entries(SHIP_DATABASE)
        .filter(([, ship]) => ship.size === size)
        .reduce((acc, [id, ship]) => { acc[id] = ship; return acc; }, {});
}

/**
 * Get ships filtered by role and size
 */
export function getShipsByRoleAndSize(role, size) {
    return Object.entries(SHIP_DATABASE)
        .filter(([, ship]) => ship.role === role && ship.size === size)
        .reduce((acc, [id, ship]) => { acc[id] = ship; return acc; }, {});
}

/**
 * Get a sorted list of ships by price
 */
export function getShipsByPrice(ascending = true) {
    return Object.entries(SHIP_DATABASE)
        .sort((a, b) => ascending ? a[1].price - b[1].price : b[1].price - a[1].price);
}
